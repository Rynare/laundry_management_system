    public void loadTablePelangganFromFilter() {
        String query = "SELECT * FROM searchPelanggan";
        String userInput = searchBar_pelanggan.getText();
        String tipeKondisi = comboBoxControl.getComboBoxValue(comboBoxPelanggan);
        String kondisi = "";

        if (!userInput.isEmpty()) {
            System.out.println(tipeKondisi);
            switch (tipeKondisi) {
                case "Nomer Telpon":
                    kondisi += " WHERE searchPelanggan.`Nomer Telpon` = " + userInput;
                    break;
                case "Nama Pelanggan":
                    kondisi += " WHERE searchPelanggan.`Nama` LIKE '%" + userInput + "%'";
                    break;
                case "Alamat":
                    kondisi += " WHERE searchPelanggan.`Alamat` LIKE '%" + userInput + "%'";
                    break;
                default:
                    break;
            }

            try {
                rs = koneksiDB.getData(query + kondisi);
                loadTablePelanggan(query + kondisi, TableUpdateMode.ALWAYS_UPDATE);
                System.out.println("kondisi : " + kondisi);
            } catch (SQLException ex) {
                System.out.println("Gagal menjalankan query: " + ex.getMessage());
            } finally {
                koneksiDB.closeConnection();
            }
        } else {
            tableControl.loadTableFromDB(tabelPelanggan, TableUpdateMode.ALWAYS_UPDATE, "SELECT * FROM searchPelanggan");
        }
    }
    
    public void loadTablePelanggan(String query, tableControl.TableUpdateMode mode, Object... value) {
        tableControl.loadTableFromDB(tabelPelanggan, mode, query, value);
    }

