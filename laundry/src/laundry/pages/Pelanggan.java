    private void kButton15MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kButton15MouseClicked
        // TODO add your handling code here:
        int baris = tabelPelanggan.getSelectedRow();
        if (baris >= 0) {
            String nama = tabelPelanggan.getValueAt(baris, 1).toString();
            int option = dialogControl.showWarningOption(parent, "Konfirmasi", "Yakin ingin menghapus \"" + nama + "\"");
            if (option == JOptionPane.YES_OPTION) {
                int id = (int) tabelPelanggan.getValueAt(baris, 0);
                String query = "DELETE FROM pelanggan WHERE pelanggan.id_pelanggan = ?";
                boolean isDeleted = queryControl.deleteData(koneksiDB, query, id);
                if (isDeleted) {
                    tableControl.deleteTableRow(tabelPelanggan,baris);
                    Id_pelanggan_field.setText("");
                    nama_pelanggan_field.setText("");
                    no_telpon_pelanggan_field.setText("");
                    alamat_pelanggan_field.setText("");
                }
            }
        } else {
            dialogControl.showWarningMessage(parent, "Harap pilih kolom yang ingin dihapus");
        }

    }//GEN-LAST:event_kButton15MouseClicked

