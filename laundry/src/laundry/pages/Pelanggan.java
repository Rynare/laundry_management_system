    private void kButton14MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kButton14MouseClicked
        // TODO add your handling code here:
        String id_pelanggan = "";
        String nama_pelanggan = insert_nama_pelanggan_field.getText();
        String no_telpon = insert_no_telpon_pelanggan_field.getText();
        String alamat = insert_alamat_pelanggan_field.getText();

        if (!(nama_pelanggan.isEmpty() || no_telpon.isEmpty() || alamat.isEmpty())) {
            try {
                String getIdPelangganQuery = "SELECT ID FROM searchpelanggan ORDER BY ID DESC LIMIT 1";
                ResultSet resultSet = koneksiDB.getData(getIdPelangganQuery);
                if (resultSet.next()) {
                    id_pelanggan = Integer.toString(Integer.parseInt(resultSet.getString("ID")) + 1);
                    String queryInsertPelanggan = "INSERT INTO pelanggan (id_pelanggan, nama_pelanggan, no_telp, alamat) VALUES (?, ?, ?, ?)";
                    Object[] valueInsertPelanggan = {id_pelanggan, nama_pelanggan, no_telpon, alamat};
                    boolean isInsert = queryControl.insertData(koneksiDB, queryInsertPelanggan, valueInsertPelanggan);
                    if (isInsert) {
                        dialogControl.showSuccessMessage(parent, "Data Pelanggan berhasil ditambah.");
                        insert_nama_pelanggan_field.setText("");
                        insert_no_telpon_pelanggan_field.setText("");
                        insert_alamat_pelanggan_field.setText("");
                    }
                }
            } catch (SQLException ex) {
                dialogControl.SQLFailedShow(parent, ex);
            } finally {
                koneksiDB.closeConnection();
            }
        } else {
            dialogControl.showWarningMessage(parent, "Harap isi semua kolom!!!");
        }
    }//GEN-LAST:event_kButton14MouseClicked

