    private void rSMaterialButtonRectangle2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangle2MouseClicked
        // TODO add your handling code here:
        String username = InputDaftarUsername.getText();
        String nama_laundry = InputDaftarNamaLaundry.getText();
        String alamat = InputDaftarAlamat.getText();
        String no_telp = InputDaftarTelp.getText();
        String password = InputDaftarPassword.getText();
        String re_type_pass = InputDaftarPassword.getText();

        if (!(username.isEmpty() || nama_laundry.isEmpty() || alamat.isEmpty() || no_telp.isEmpty() || password.isEmpty() || re_type_pass.isEmpty())) {
            try {
                rs = koneksi.getDataWithPreparedStatement("SELECT COUNT(username) AS username_count FROM laundry WHERE username = ?", username);
                if (rs.next()) {
                    if (rs.getInt(1) > 0) {
                        int option = dialogControl.showWarningOption(this, "Konfirmasi", "USERNAME Sudah tersedia\nIngin menggunakan USERNAME dari kami?");
                        if (option == JOptionPane.YES_OPTION) {
                            InputDaftarUsername.setText(username + rs.getInt(1));
                        }
                    } else if (rs.getInt(1) == 0) {
                        if (re_type_pass.equals(password)) {
                            String query = "CALL createAccount(?, ?, ?, ?, ?)";
                            Object[] value = {nama_laundry, no_telp, alamat, password, username};
                            pertanyaan.createAccQuery = query;
                            pertanyaan.createAccValue = value;
                            pertanyaan.Username = InputDaftarUsername.getText();
                            pertanyaan.setParent(this);
                            System.out.println("query : " + query);
                            if (!pertanyaan.isVisible()) {
                                pertanyaan.setVisible(true);
                            }
                        } else {
                            dialogControl.showWarningMessage(this, "Password tidak cocok");
                        }
                    }
                }
            } catch (SQLException e) {
                e.getMessage();
            }
        } else {
            dialogControl.showWarningMessage(this, "Harap isi semua data yang diperlukan!!!");
        }
    }//GEN-LAST:event_rSMaterialButtonRectangle2MouseClicked

