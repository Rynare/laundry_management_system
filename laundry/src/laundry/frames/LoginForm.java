    private void DaftarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DaftarMouseClicked
        // TODO add your handling code here:
        try {
            String query = "SELECT COUNT(laundry.id_laundry) from laundry";
            rs = koneksi.getData(query);
            if (rs.next() && !demoCheck.isSelected()) {
                dialogControl.showWarningMessage(this, "Anda sudah pernah mendaftar");
            } else {
                openPanel(SignUpForm);
            }
        } catch (SQLException e) {
            dialogControl.SQLFailedShow(this, e);
        } finally {
            koneksi.closeConnection();
        }
    }//GEN-LAST:event_DaftarMouseClicked

