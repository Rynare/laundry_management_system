    public void login(String username, String password) {
        if (!(username.isEmpty() || password.isEmpty())) {
            String query = "CALL Login(?, ?)";
            Object[] value = {username, password};
            int id_laundry = 0;
            try {
                rs = koneksi.getDataWithPreparedStatement(query, value);

                if (rs.next()) {
                    String result = rs.getString("Result");
                    if (result.equals("berhasil")) {
                        rs = koneksi.getDataWithPreparedStatement("SELECT id_laundry FROM laundry WHERE username = ?", username);
                        if (rs.next()) {
                            id_laundry = rs.getInt("id_laundry");
                            MainContent laundry = new laundry.frames.MainContent();
                            laundry.setData(id_laundry);
                            laundry.prepare();
                            laundry.setVisible(true);
                            this.setVisible(false);
                        }
                    } else if (result.equals("gagal")) {
                        dialogControl.showWarningMessage(this, "USERNAME atau PASSWORD yang anda masukkan salah!!!");
                    }
                }
            } catch (SQLException e) {
                // dialogControl.SQLFailedShow(this, e);
                e.getSQLState();
            } finally {
                koneksi.closeConnection();
            }

        } else {
            dialogControl.showWarningMessage(this, "Harap Isi Semua Kolom!!!");
        }
    }

