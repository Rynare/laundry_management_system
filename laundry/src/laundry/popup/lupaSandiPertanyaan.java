    public void checkAnswer() {
        String pertanyaan = pertanyaan_field.getText();
        String jawaban = jawaban_field.getText();
        if (!(pertanyaan.isEmpty() || jawaban.isEmpty())) {
            try {
                String query = "CALL CekPertanyaanDanJawaban(?, ?)";
                rs = koneksi.getDataWithPreparedStatement(query, id_pertanyaan, jawaban);
                if (rs.next()) {
                    if (rs.getString("Result").equals("benar")) {
                        if (!lupaSandiUbahSandi.isVisible()) {
                            lupaSandiUbahSandi.id_laundry = id_laundry;
                            lupaSandiUbahSandi.setVisible(true);
                        }
                        this.dispose();
                    } else if (rs.getString("Result").equals("salah")) {
                        dialogControl.showWarningMessage(this, "Jawaban yang anda berikan SALAH!!!");
                    }
                }
            } catch (SQLException e) {
                dialogControl.SQLFailedShow(this, e);
            } finally {
                koneksi.closeConnection();
            }
        } else {
            dialogControl.showWarningMessage(this, "Harap Isi JAWABAN anda!!!");
        }
    }
    
