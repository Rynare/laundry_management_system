    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try {
            System.out.println("" + Username);
            ResultSet rsGetId = koneksi.getDataWithPreparedStatement("SELECT id_laundry from laundry WHERE username = ?", Username);
            if (rsGetId.next()) {
                this.id_laundry = rsGetId.getInt("id_laundry");
                String query = "SELECT * FROM lupa_sandi WHERE id_laundry = ?";
                ResultSet rsGetQ = koneksi.getDataWithPreparedStatement(query, this.id_laundry);
                if (rsGetQ.next()) {
                    id_pertanyaan = rsGetQ.getInt("id_pertanyaan");
                    pertanyaan_field.setText(rsGetQ.getString("pertanyaan"));
                }
            }
        } catch (SQLException e) {
            dialogControl.SQLFailedShow(this, e);
            this.dispose();
        } finally {
            koneksi.closeConnection();
        }
    }//GEN-LAST:event_formComponentShown

