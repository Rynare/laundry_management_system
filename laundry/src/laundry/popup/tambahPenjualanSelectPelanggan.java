    private void tabelPelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPelangganMouseClicked
        clickCount++;
        if (clickCount == 2) {
            if (prevRow == tabelPelanggan.getSelectedRow()) {
                int baris = tabelPelanggan.getSelectedRow();
                System.out.println("Double-clicked on row: " + prevRow);
                int id = (int) tabelPelanggan.getValueAt(baris, 0);
                String nama_pelanggan = (String) tabelPelanggan.getValueAt(baris, 1);
                String alamat = (String) tabelPelanggan.getValueAt(baris, 2);
                String no_telp = (String) tabelPelanggan.getValueAt(baris, 3);
                submitToTambahPenjualan(id, nama_pelanggan, alamat, no_telp);
                this.dispose();
            }
            clickCount = 0;
        } else {
            prevRow = tabelPelanggan.getSelectedRow();
            Timer timer = new Timer();
            timer.schedule(new TimerTask() {
                @Override
                public void run() {
                    clickCount = 0;
                }
            }, 500); // Waktu dalam milidetik untuk mereset clickCount jika tidak ada double-click berikutnya dalam 200ms
        }
    }//GEN-LAST:event_tabelPelangganMouseClicked

