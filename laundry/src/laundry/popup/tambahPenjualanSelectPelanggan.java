    public void loadTableFromFilter() {
        String query = "SELECT * FROM searchPelanggan";
        String userInput = SearchBarPelanggan.getText();
        String tipeKondisi = getComboboxValue(comboBoxPelanggan);
        String kondisi = "";

        if (!userInput.isEmpty()) {
            if (tipeKondisi.equals("Nomer Telpon")) {
                kondisi += " WHERE searchPelanggan.`Nomer Telpon` = " + userInput;
            } else if (tipeKondisi.equals("Nama_Pelanggan")) {
                kondisi += " WHERE searchPelanggan.`Nama` LIKE '%" + userInput + "%'";
            }else if (tipeKondisi.equals("Alamat")){
                kondisi += " WHERE searchPelanggan.`Alamat` LIKE '%" + userInput + "%'";
            }

            try {
                ResultSet resultSet = koneksiDB.getData(query + kondisi);
                loadTablePelanggan(query + kondisi, TableUpdateMode.ALWAYS_UPDATE);
                resultSet.close();
            } catch (SQLException ex) {
                System.out.println("Gagal menjalankan query: " + ex.getMessage());
            } finally {
                koneksiDB.closeConnection();
            }
        }else{
            tableControl.loadTableFromDB(tabelPelanggan, TableUpdateMode.ALWAYS_UPDATE, "SELECT * FROM searchPelanggan");
        }
    }

