public void showBarcode(String file_source_path) {
        File imageFile = new File(file_source_path);
        if (imageFile.exists()) {
            ImageIcon imgThisImg = new ImageIcon(file_source_path);
            if (imgThisImg.getIconHeight() > 0) {
                barcode_label.setIcon(imgThisImg);
                barcode_label.setText("");
            } else {
                // Handle the case when the image cannot be loaded
                System.out.println("Failed to load the image");
            }
        } else {
            // Handle the case when the image file does not exist
            System.out.println("Image file does not exist");
        }
    }

    enum BarcodeFileExtension {
        PNG
    }

    public String createBarcode(String destination_folder, String barcode_filename, BarcodeFileExtension file_extension) {
        try {
            Linear barcode = new Linear();
            barcode.setType(Linear.CODE128);
            barcode.setData(barcode_filename);
            barcode.setI(11.0f);
            barcode.renderBarcode(destination_folder + barcode_filename + "." + file_extension.toString().toLowerCase());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Cetak Barcode: " + e.getMessage());
        }
        return destination_folder + barcode_filename + "." + file_extension.toString().toLowerCase();
    }

