    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        int id_pelanggan;
        String no_telp = no_telp_field.getText();

        try {
            String query = "SELECT penjualan.id_pelanggan FROM penjualan WHERE id_penjualan = " + id_penjualan_field.getText();
            ResultSet rs = koneksi.getData(query);

            if (rs.next()) {
                id_pelanggan = rs.getInt("id_pelanggan");

                String updateQuery = "UPDATE pelanggan SET no_telp = ? WHERE id_pelanggan = ?";
                Object[] value = {no_telp, id_pelanggan};
                queryControl.updateData(koneksi, updateQuery, value);
            }
        } catch (SQLException e) {
            // Handle the exception appropriately
            e.printStackTrace();
        } finally {
            koneksi.closeConnection();
        }
    }//GEN-LAST:event_jButton3MouseClicked

