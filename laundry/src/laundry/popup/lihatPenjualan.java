    private void kButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kButton2MouseClicked
        // TODO add your handling code here:
        this.setAlwaysOnTop(false);
        JasperPrint jasperPrint;
        try {
            String report = "C:\\this is college\\S2\\Workshop Pemrogaman Aplikasi\\laundry\\src\\report\\Nota.jrxml";
            HashMap<String, Object> parameters = new HashMap<>();
            parameters.put("id_penjualan", id_penjualan_field.getText());

            JasperReport compiled_report = JasperCompileManager.compileReport(report);
            jasperPrint = JasperFillManager.fillReport(compiled_report, parameters, koneksi.getConnection());
            if (auto_print_check.isSelected()) {
                JasperPrintManager.printReport(jasperPrint, false);
            } else {
                JasperPrintManager.printReport(jasperPrint, true);
            }
        } catch (JRException e) {
            e.getMessage();
        }
    }//GEN-LAST:event_kButton2MouseClicked

